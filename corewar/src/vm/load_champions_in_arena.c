#include "corewar.h"

void load_champs_to_arena(vm_t *vm, options_t options, champion_body_t *bodies)
{
    uint32_t standard_offset = MEM_SIZE / options.champions.len;
    u8 nb_predefined = 0;
    for (u32 i = 0; i < options.champions.len; i++)
        nb_predefined += (options.champions.champions[i].address != -1) ? 1 : 0;
    if (!nb_predefined)
        for (u32 i = 0; i < options.champions.len; i++)
            my_memcpy(vm->arena + (i * standard_offset), bodies[i].body,
                bodies[i].len);
}
