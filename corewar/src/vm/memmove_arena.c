#include "corewar.h"

void *memmove_from_arena(void *dest, u8 *src, int32_t pos, size_t n)
{
    char *dest_c = dest;
    const char *src_c = src;
    size_t i = 0;

    for (; i < n; i++)
        dest_c[i] = src_c[(pos + i) % MEM_SIZE];
    return dest;
}

void *memmove_to_arena(u8 *dest, void *src, int32_t pos, size_t n)
{
    char *dest_c = dest;
    const char *src_c = src;
    size_t i = 0;

    for (; i < n; i++)
        dest_c[(pos + i) % MEM_SIZE] = src_c[i];
    return dest;
}
