#include <stddef.h>

void *my_memcpy(void *restrict dest, const void *restrict src, size_t n)
{
    char *sdest = (char*)dest;
    const char *ssrc = (char*)src;
    if (sdest < ssrc)
        while (n--)
            *sdest++ = *ssrc++;
    return (dest);
}
