#include <stddef.h>

void* memcpy(void* dest, const void* src, size_t n)
{
    char* dest_char = (char*)dest;
    const char* src_char = (const char*)src;
    for (size_t i = 0; i < n; i++) {
        dest_char[i] = src_char[i];
    }
    return dest;
}

