#include <errno.h>
#include <limits.h>
#include <stdbool.h>

bool is_space(char c)
{
    if (c == 32 || c == '\f' || c == 10 || c == '\r' || c == '\t' || c == '\v')
        return true;
    return false;
}

bool is_digit(char c)
{
    if (c >= '0' && c <= '9')
        return true;
    return false;
}

bool is_alpha(char c)
{
    if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
        return true;
    return false;
}

char to_upper(char c)
{
    if (c >= 'a' && c <= 'z')
        return c - 32;
    return c;
}

long my_strtol(const char *s, char **ep, int b)
{
    unsigned long r = 0; bool neg = false; int d;
    while (is_space(*s)) s++;
    if (*s == '-' || *s == '+') neg = (*s++ == '-');
    if (!b)
        b = *s == '0' ? (*(s + 1) == 'x' || *(s + 1) == 'X' ?
        (++s, 16) : 8) : 10;
    else if (b == 16 && *s == '0' && (*(s + 1) == 'x' ||
        *(s + 1) == 'X')) s += 2;
    for (; *s; s++) {
        d = is_digit(*s) ? *s - '0' : is_alpha(*s) ?
            to_upper(*s) - 'A' + 10 : -1;
        if (d < 0 || d >= b) break;
        r = (r > (ULONG_MAX - d) / b) ? (errno = ERANGE, ULONG_MAX) : r * b + d;
    } if (ep) *ep = (char *)s;
    if (neg) r = (r > (unsigned long)LONG_MIN) ? errno = ERANGE, LONG_MIN : -r;
    else if (r > LONG_MAX) errno = ERANGE, r = LONG_MAX;
    return (long)r;
}
