double my_pow(double val, double exp)
{
    if (val == 0) return (val);
    if (exp == 0)
        return ((double)1);
    double res = 1;
    if (exp > 0) {
        for (int i = 0; i < exp; i++, res *= val);
        return (res);
    }
    for (int i = 0; i > exp; i--, res /= val);
    return (res);
}

