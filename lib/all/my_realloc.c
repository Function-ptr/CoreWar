#include <stddef.h>
#include <stdlib.h>
#include <errno.h>
#include <stdint.h>

void *my_memmove(void *dest, const void *src, size_t n);

void *my_realloc(void *ptr, size_t size, size_t new_size)
{
    if (new_size == 0 && ptr != NULL) {
        free(ptr);
        return NULL;
    }
    if (new_size < size)
        return ptr;
    void *new = malloc(new_size);
    if (new == NULL)
        return (NULL);
    if (ptr == NULL)
        return new;
    if (new_size >= size) {
        my_memmove(new, ptr, size);
        free(ptr);
    }
    return (new);
}

void *my_reallocarray(void *ptr, size_t nmemb, size_t size, size_t oldnmemb)
{
    if (nmemb > UINT64_MAX / size || oldnmemb > UINT64_MAX / size) {
        errno = ENOMEM;
        return NULL;
    }
    if (nmemb && (SIZE_MAX / nmemb < size)) {
        errno = ENOMEM;
        return NULL;
    }
    void *new = my_realloc(ptr, oldnmemb * size, nmemb * size);
    return (new);
}
