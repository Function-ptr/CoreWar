#include <stddef.h>

void *my_memmove(void *dest, const void *src, size_t n)
{
    char *d = dest;
    char *s = (void*)src;
    if (d < s)
        while (n--)
            *d++ = *s++;
    else {
        char *lasts = s + (n - 1);
        char *lastd = d + (n - 1);
        while (n--)
            *lastd-- = *lasts--;
    }
    return dest;
}
