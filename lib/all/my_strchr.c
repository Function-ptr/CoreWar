#include <stddef.h>

char *my_strchr(char *s, int c)
{
    for (int i = 0; s[i] != 0; i++)
        if (s[i] == c)
            return (&s[i]);
    return (NULL);
}

char *my_strrchr(char *s, int c)
{
    char *last = NULL;
    for (int i = 0; s[i] != 0; i++) {
        if (s[i] == c)
            last = &s[i];
    }
    return last;
}

char *my_dstrchr(char *start, char *endptr, char c)
{
    for (; *start && start != endptr; start++) {
        if (*start == c)
            return start;
    }
    return NULL;
}

