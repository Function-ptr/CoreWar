#include "include/printf.h"

char *itos(int nb)
{
    if (nb <= 0) {
        char *res = malloc(sizeof(char) * 2);
        res[0] = '0';
        res[1] = '\0';
        return (res);
    }
    int len = my_getnbr_len(nb);
    char *res = malloc(sizeof(char) * (len + 1));
    for (int i = 0; i < len; i++, nb *= 10)
        res[i] = ((nb / (int)my_power_it(10, len - 1)) % 10) + 48;
    res[len] = '\0';
    return (res);
}

