#include "asm.h"
#include "my.h"

char *get_filename(char *arg)
{
    char *dup = my_strdup(arg);
    if (!dup)
        return NULL;
    if (my_strstr(arg, ".s"))
        return dup;
    int len = my_strlen(dup);
    dup = my_reallocarray(dup, len + 3, sizeof(char), len + 1);
    if (!dup) {
        free(dup);
        return NULL;
    }
    my_strcpy(dup + len, ".s");
    return dup;
}
