#include "asm.h"
#include "my.h"

const char *keys[NUM_KEYS] = {"live", "ld", "st", "add", "sub", "and",
    "or", "xor", "zjmp", "ldi", "sti", "fork",
    "lld", "lldi", "lfork", "aff"};

unsigned long hash(const char *str)
{
    for (unsigned long i = 0; i < NUM_KEYS; ++i) {
        if (my_strcmp(keys[i], str) == 0)
            return i;
    }

    return TABLE_SIZE;
}

