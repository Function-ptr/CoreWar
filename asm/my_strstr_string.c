#include "asm.h"
int is_needle_string(int pos, char const *to_find, string_t str)
{
    for (int i = 0; pos + i < (int)str.len && to_find[i] != '\0'; i++) {
        if (str.str[pos + i] != to_find[i])
            return (0);
    }
    return (1);
}

int my_strstr_string(string_t str, char const *to_find)
{
    int i = 0;

    if (to_find == NULL || *to_find == 0)
        return (-1);
    for (; i < (int)str.len; i++) {
        int test = is_needle_string(i, to_find, str);
        if (test)
            return (i);
    }
    return (-1);
}


