#ifndef ASM_ASM_H
    #define ASM_ASM_H
    #include <unistd.h>
    #include <stddef.h>
    #include <stdint.h>
    #include <stdio.h>
    #include "op.h"

typedef uint8_t byte;
typedef uint16_t word;
typedef uint32_t dword;
typedef uint64_t qword;

    #define TABLE_SIZE 16

    typedef struct {
        char* str;
        uint64_t len;
    } string_t;

    typedef struct Entry {
        char *key;
        op_t* value;
        struct Entry *next;
    } Entry;

    typedef struct HashTable {
        Entry* buckets[TABLE_SIZE];
    } hash_table;

    unsigned long hash(char *str);
    hash_table* create_table(void);
    void insert(hash_table* table, char* key, op_t* value);
    op_t* lookup(hash_table *table, char *key);
    void delete(hash_table *table, char *key);
    void free_table(hash_table *table);
    string_t *create_string(char *str);
    string_t *string_dup(string_t *str);

char *get_filename(char *arg);
char *read_s_file(char *filename);

#endif //ASM_ASM_H
